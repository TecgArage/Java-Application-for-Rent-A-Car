    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rent_a_car","root","");
            
            int row = tb1.getSelectedRow();
            String value = (tb1.getModel().getValueAt(row,0).toString());
            
            String sql = "UPDATE `driver` SET `First_Name` = ?, `Last_Name` = ?, `DOB` = ?, `Gender` = ?, `License_No` = ?, `Contact_No` = ?, `Address` = ?, `Availability` = ? " + value;
            
            PreparedStatement pstmt = con.prepareStatement(sql);

            pstmt.setString(1,fn.getText());
            pstmt.setString(2,ln.getText());
            pstmt.setInt(3,Integer.parseInt(dob.getText()));
            
             
            pstmt.setString(4,gender.getText());
            pstmt.setInt(5,Integer.parseInt(licenum.getText()));
            pstmt.setInt(6,Integer.parseInt(telnum.getText()));
            pstmt.setString(7,address.getText());
            pstmt.setInt(8,Integer.parseInt(avail.getText()));
            

           

            pstmt.executeUpdate();
            
            DefaultTableModel tblmodel = (DefaultTableModel)tb1.getModel();
            tblmodel.setRowCount(0);
            DisplayTable();
            
            JOptionPane.showMessageDialog(null,"Updated Driver Details Succesfully");
            con.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

