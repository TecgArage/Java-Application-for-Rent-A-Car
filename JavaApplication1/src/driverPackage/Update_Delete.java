    private void DisplayTable(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/rent_a_car","root","");
            
            String sql = "select * from driver";
            PreparedStatement pstmt = con.prepareStatement(sql);
            
            ResultSet rs = pstmt.executeQuery();
            
            while(rs.next()){
                
                String fn = rs.getString("First_Name");
                String ln = rs.getString("Last_Name");
                String dob = String.valueOf(rs.getInt("DOB"));
                String gender = rs.getString("Gender");
                String licenum = String.valueOf(rs.getInt("License_No"));
                String telnum = String.valueOf(rs.getInt("Contact_No"));
                String address = rs.getString("Address");
                String avail = String.valueOf(rs.getInt("Availability"));
                
                String tbDataArr[] = {fn,ln,dob,gender,licenum,telnum,address,avail};
                DefaultTableModel tblModel = (DefaultTableModel)tb1.getModel();
                
                tblModel.addRow(tbDataArr);
                
            }
            
            con.close();
        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    
    }


